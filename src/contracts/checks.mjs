export function getCheckById(checkId) {
  return CHECK_CATALOG.find((c) => c.check_id === checkId) ?? null;
}

export function isCustomerRunnable(check) {
  if (!check) return false;
  if (check.risk_class === 'soc_gated') return false;
  if (check.safety_constraints?.customer_runnable === false) return false;
  return true;
}

export function evaluateCheckPrerequisites(check, { onlineAgents = [] } = {}) {
  const missing = [];
  for (const prereq of check.prerequisites ?? []) {
    if (prereq.startsWith('agent_mode:')) {
      const mode = prereq.replace('agent_mode:', '');
      const has = onlineAgents.some((a) => (a.capabilities ?? []).includes(mode));
      if (!has) missing.push(prereq);
    }
  }
  return missing;
}
