const DEFAULT_SAFE_STOP_CONDITIONS = Object.freeze([
  'max_events_reached',
  'max_duration_elapsed',
  'customer_cancel',
  'tenant_kill_switch',
]);

const DEFAULT_SOC_STOP_CONDITIONS = Object.freeze([
  'soc_kill_switch',
  'abort_criteria_met',
  'provider_stop_signal',
  'max_approved_duration',
]);

function safeCheck(def) {
  const probe_profile = buildProbeProfile(
    def.probe_profile ?? { kind: 'metadata_marker', max_requests: 1, timeout_ms: 5000 },
  );
  const stop_conditions = def.stop_conditions ?? [...DEFAULT_SAFE_STOP_CONDITIONS];
  const safety_constraints = {
    max_events: 3,
    max_duration_seconds: 90,
    max_concurrent_runs_per_target_group: 1,
    ...(def.safety_constraints ?? {}),
  };
  return {
    safety_class: 'safe',
    risk_class: 'safe',
    prerequisites: def.prerequisites ?? [],
    remediation_template: def.remediation_template ?? 'Review edge protection and agent placement for this vector.',
    explanation_template: def.explanation_template ?? 'Simulated probe metadata correlated with agent observation.',
    supported_targets: def.supported_targets ?? ['fqdn'],
    required_customer_setup: def.required_customer_setup ?? [],
    evidence_required: def.evidence_required ?? ['probe_result', 'agent_observation'],
    stop_conditions,
    verdict_logic:
      def.verdict_logic ??
      'Verdict when probe external_result and agent observation align with target expected_behavior.',
    safety_constraints,
    probe_profile,
    ...def,
    stop_conditions,
    safety_constraints,
    probe_profile,
  };
}

function socGated(def) {
  const { probe_profile: _ignoredProbe, ...rest } = def;
  const required_customer_setup = rest.required_customer_setup ?? [
    'soc_authorization_pack',
    'declared_target_group_scope',
  ];
  const evidence_required = rest.evidence_required ?? ['approval_artifact', 'soc_runbook_ack'];
  const stop_conditions = rest.stop_conditions ?? [...DEFAULT_SOC_STOP_CONDITIONS];
  const safety_constraints = { customer_runnable: false, ...(rest.safety_constraints ?? {}) };
  return {
    safety_class: 'soc_gated',
    risk_class: 'soc_gated',
    prerequisites: ['soc_approval_required'],
    remediation_template:
      rest.remediation_template ?? 'Submit a SOC-gated high-scale request with authorization pack.',
    explanation_template: rest.explanation_template ?? 'Not runnable via customer test-runs API.',
    required_customer_setup,
    evidence_required,
    stop_conditions,
    safety_constraints,
    ...rest,
    safety_class: 'soc_gated',
    risk_class: 'soc_gated',
    required_customer_setup,
    evidence_required,
    stop_conditions,
    safety_constraints,
  };
}

