/** Safe check catalog — metadata-only validation; no live attack traffic in developer validation mode. */

export const ALLOWED_PROBE_PROFILE_KINDS = Object.freeze([
  'http_head',
  'tcp_connect',
  'dns_resolve',
  'metadata_marker',
  'udp_probe',
  'quic_reachability',
  'alert_webhook_ping',
  'ops_readiness',
  'ownership_challenge',
  'tls_session',
  'http2_settings',
]);

const ALLOWED_OPS_READINESS_SCENARIOS = new Set(['runbook_contacts', 'kill_switch_readiness']);

const ALLOWED_PROBE_KINDS = new Set(ALLOWED_PROBE_PROFILE_KINDS);
export const MAX_PROBE_PROFILE_TIMEOUT_MS = 5000;
export const MAX_PROBE_PROFILE_REQUESTS = 5;

export const WAF_SAFE_PROBE_METADATA_KEYS = Object.freeze([
  'scenario_family',
  'marker_type',
  'expected_action',
  'nonce_hash_only',
  'collect',
]);

const ALLOWED_WAF_SCENARIO_FAMILIES = new Set([
  'marker',
  'fingerprint',
  'origin_bypass',
  'rate_limit_marker',
]);

const ALLOWED_WAF_MARKER_TYPES = new Set(['header', 'path', 'query', 'user_agent']);

const ALLOWED_WAF_EXPECTED_ACTIONS = new Set([
  'block',
  'challenge',
  'rate_limit',
  'log_only_expected',
  'allow_expected',
]);

const ALLOWED_WAF_COLLECT_KEYS = new Set([
  'status_code',
  'server_header',
  'waf_product_hint',
  'tls_fingerprint_hint',
]);

export const WAF_SAFE_CHECK_IDS = Object.freeze([
  'l7.waf_marker_rule.safe',
  'waf.fingerprint.safe',
  'waf.marker_rule.safe',
  'waf.origin_bypass.safe',
  'waf.low_rate_limit.safe',
]);

function applyWafSafeProbeMetadata(profile, meta = {}) {
  const scenario = meta.scenario_family;
  if (typeof scenario === 'string' && ALLOWED_WAF_SCENARIO_FAMILIES.has(scenario)) {
    profile.scenario_family = scenario;
  }
  const markerType = meta.marker_type;
  if (typeof markerType === 'string' && ALLOWED_WAF_MARKER_TYPES.has(markerType)) {
    profile.marker_type = markerType;
  }
  const expected = meta.expected_action;
  if (typeof expected === 'string' && ALLOWED_WAF_EXPECTED_ACTIONS.has(expected)) {
    profile.expected_action = expected;
  }
  if (meta.nonce_hash_only === true) {
    profile.nonce_hash_only = true;
  }
  if (Array.isArray(meta.collect)) {
    const collect = meta.collect
      .filter((key) => typeof key === 'string' && ALLOWED_WAF_COLLECT_KEYS.has(key))
      .slice(0, 8);
    if (collect.length > 0) profile.collect = collect;
  }
}

export function buildProbeProfile({
  kind,
  max_requests = 1,
  timeout_ms = 5000,
  marker,
  scenario,
  scenario_family,
  marker_type,
  expected_action,
  nonce_hash_only,
  collect,
} = {}) {
  if (!ALLOWED_PROBE_KINDS.has(kind)) {
    throw new Error(`invalid probe profile kind: ${kind}`);
  }
  const cappedRequests = Math.min(
    Math.max(1, Number(max_requests) || 1),
    MAX_PROBE_PROFILE_REQUESTS,
  );
  const cappedTimeout = Math.min(
    Math.max(100, Number(timeout_ms) || MAX_PROBE_PROFILE_TIMEOUT_MS),
    MAX_PROBE_PROFILE_TIMEOUT_MS,
  );
  const profile = {
    kind,
    max_requests: cappedRequests,
    timeout_ms: cappedTimeout,
  };
  if (marker) profile.marker = String(marker).slice(0, 128);
  if (kind === 'http_head') profile.method = 'HEAD';
  if (kind === 'ops_readiness' && typeof scenario === 'string' && ALLOWED_OPS_READINESS_SCENARIOS.has(scenario)) {
    profile.scenario = scenario;
  }
  applyWafSafeProbeMetadata(profile, {
    scenario_family,
    marker_type,
    expected_action,
    nonce_hash_only,
    collect,
  });
  return profile;
}

