#!/usr/bin/env node
/**
 * Runs verification-plan steps 1–6 for P0/P1 capability probes and writes artifacts to SCRATCH.
 * Usage: node scripts/capture-probe-verification-evidence.mjs [--scratch /path/to/dir]
 *
 * Set ASTRANULL_SKIP_PUBLIC_DNS=1 only in sandboxed environments without outbound DNS;
 * the script fails closed when public AXFR live I/O cannot run.
 */

import { spawnSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { getCheckById } from '../src/contracts/checks.mjs';

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const scratchArgIdx = process.argv.indexOf('--scratch');
const scratch =
  scratchArgIdx >= 0 && process.argv[scratchArgIdx + 1]
    ? resolve(process.argv[scratchArgIdx + 1])
    : resolve(repoRoot, 'tmp/probe-verification-evidence');

mkdirSync(scratch, { recursive: true });

function run(cmd, args, logName, { allowFail = false } = {}) {
  const result = spawnSync(cmd, args, {
    cwd: repoRoot,
    encoding: 'utf8',
    env: process.env,
  });
  const output = `${result.stdout ?? ''}${result.stderr ?? ''}`;
  writeFileSync(join(scratch, logName), output);
  if (result.status !== 0 && !allowFail) {
    throw new Error(`${cmd} ${args.join(' ')} failed (exit ${result.status}); see ${logName}`);
  }
  return { output, status: result.status ?? 1 };
}

function runNodeEval(source, logName, { allowFail = false } = {}) {
  return run('node', ['--input-type=module', '-e', source], logName, { allowFail });
}

